void solve() {
    string s; cin >> s;
    if (s.length() == 1) {
        cout << 0; return;
    }
    ll n = 0;
    for (char c: s) 
        n += (c-'0');
    ll ops = 1;

    while (n >= 10) {
        ll temp = n;
        n = 0;
        while (temp) {
            n += temp%10;
            temp /= 10;
        }
        ops++;
    }

    cout << ops;
}

