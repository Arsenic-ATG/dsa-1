void solve() {
    int a, b; cin >> a >> b;
    int s = a+b;
    while (a and b) {
        int ar = a%10, br = b%10, sr = s%10;
        a /= 10; b /= 10; s /= 10;
        if (
            (!ar and (br or sr))
            or
            (!br and (ar or sr))
            or
            (!sr and (br or ar)) 
            ) {
                cout << "NO"; return;
            }
    }
    cout << "YES";
}

