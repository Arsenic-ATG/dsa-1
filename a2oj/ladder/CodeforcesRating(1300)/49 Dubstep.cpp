void solve() {
    string s; cin >> s;
    int n = (int)s.length();
    string op;
    for (int i=0; i<n; ++i) {
        if (s[i] == 'W' and i+2 < n) {
            if (s[i+1] == 'U' and s[i+2] == 'B') {
                i+=2;
                if (op.length() > 0 and op[op.length()-1] != ' ') 
                    op.pb(' ');
                continue;
            }
        }
        op.pb(s[i]);
    }
    cout << op;
}

