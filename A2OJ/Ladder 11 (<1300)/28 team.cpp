void solve() {
  int t;
  cin >> t;
  int problems = 0;
  while (t--) {
    int a, b, c;
    cin >> a >> b >> c;
    bool one = a and b,
         two = b and c,
         thr = a and c;

    if (one || two || thr) problems++;
  }
  cout << problems;
}

